Certains enseignants désirent limiter l'accès leur site de cours en obligeant les étudiants qui désirent s'y inscrire à entrer une "clé".
Cette clé est généralement communiquée par l'enseignant lors des séances de cours.

Malheureusement, nous ne sommes pas autorisé à vous révéler cette information.
Nous vous invitons donc à prendre contact avec l'un des gestionnaires du cours pour lui demander de vous la fournir.
