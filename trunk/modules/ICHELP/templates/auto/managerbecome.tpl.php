Si vous désirez devenir gestionnaire d'un cours existant, il suffit de suivre la procédure ci-dessous :

    1 - Incrivez-vous d'abord au cours en question
    2 - Contactez l'actuel gestionnaire afin qu'il vous donne le statut de co-gestionnaire.

La procédure est décrite dans le détail à cette adresse : http://blogs.uclouvain.be/aideicampus/manuel-enseignant/administrer-mes-espaces-de-cours/gerer-le-changement-de-titulaire-dun-cours-officiel/

Notez enfin, qu'en tant que simple intermédiaire technique, l'équipe iCampus n'est pas habilitée à opérer ce changement, mais s'il s'avère que l'ancien gestionnaire n'est plus joignable, nous pouvons le faire avec l'aval écrit d'une autorité facultaire...
