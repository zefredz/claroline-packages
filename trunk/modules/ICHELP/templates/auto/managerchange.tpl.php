En cas de changement de gestionnaire, la procédure à suivre est la suivante:

    1 - Le futur gestionnaire au cours, comme un simple étudiant (si ce n'est déjà fait).
    2 - Il demande alors à l'actuel gestionnaire de lui donner le statut de gestionnaire.
    3 - L'ancien gestionnaire peut alors se désinscrire du cours.

En tant que simple intermédiaire technique, l'équipe icampus n'est pas habilitée à opérer ce changment, mais s'il s'avère que l'ancien gestionnaire n'est plus joignable, nous pouvons le faire avec l'aval écrit d'une autorité facultaire...

