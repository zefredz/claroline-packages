Il existe deux manières de gérer le changement de titulaire d'un cours sur iCampus.
Le choix de la méthode dépendra des rapports que les deux gestionnaires entretiennent entres-eux.

Si le nouveau titulaire souhaite travailler dans la continuité de l'ancien, avec les mêmes ressources, nous vous conseillons de conserver l'espace de cours existant.

Dans ce cas, la procédure à suivre est la suivante :

    1 - Le futur gestionnaire s'inscrit au cours, comme un simple étudiant (si ce n'est déjà fait).
    2 - Il demande alors à l'actuel gestionnaire de lui donner le statut de gestionnaire.
    3 - Le nouveau gestionnaire peut alors désinscrire l'ancien du cours.
    

Si par contre, leurs dispositifs pédagogiques respectifs sont assez éloignés, il est préférable de créer un nouvel espace de cours et de désactiver de l'ancien afin d'éviter toute confusion dans l'esprit de vos étudiants.

Pour ce faire, une demande de désactivation de l'ancien cours doit nous parvenir via le helpdesk iCampus, avec accord de l'ancien titulaire.
S'il s'avère que cette personne est injoignable, une demande écrite (mail) d'une autorité facultaire est nécessaire.


