Si vous désirez partager la gestion de l'un de vos espaces de cours afin qu'un collègue enseignant, un assistant, ou encore une secrétaire, puisse y avoir les mêmes droits de modification que vous, il suffit de suivre la procédure suivante :

    - Premièrement, assurez-vous que cette personne dispose bien d'un compte sur iCampus. Pour cela, il suffit qu'elle se soit connectée au moins une fois (si celle-ci est membre de l'UCL).
    - Inscrivez-la à votre cours, ou demandez-lui de le faire.
    - Par défaut, la personne possède le profil "utilisateur" (tout comme les étudiants). Vous devez lui donner le profil de gestionnaire via l'outil "Utilisateurs".
      En regard de son nom, cliquez sur le crayon et choisissez le profil "gestionnaire" dans le menu déroulant, puis validez en cliquant sur "OK".

