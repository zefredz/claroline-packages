Si vous être inscrit(e) comme étudiant(e) à l'université, ou si vous êtes membre du personnel, vous pouvez accéder à iCampus en utilisant votre identifiant global UCL.
Lors de votre première connexion, votre compte iCampus sera automatiquement créé.

Attention : votre compte UCL doit avoir été préalablement activé !
Si ce n'est pas le cas, commencez par le faire, en allant à cette adresse : http://www.uclouvain.be/4040.

Si vous êtes extérieur(e) à l'UCL mais que vous êtes impliqué dans un programme d'étude pour lequel un accès à iCampus est nécessaire, adressez une demande de création de compte extérieur via votre responsable de programme.