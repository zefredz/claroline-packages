Si vous souhaitez qu’un utilisateur (étudiant ou enseignant) qui n’a pas de compte UCL ait accès à votre espace cours dans iCampus, il faut demander aux administrateurs d’iCampus de lui créer un compte iCampus extérieur. Cette demande devra être motivée par une demande écrite d’un enseignant UCL.

La personne devra fournir son nom, son prénom, une adresse email, le nom de l’institution auquel il appartient, sa date de naissance, un numéro de téléphone, son année d’étude.

Si vous souhaitez qu’un groupe de plusieurs utilisateurs “extérieurs” obtiennent un compte sur iCampus, faites nous parvenir un fichier de type EXCEL, contenant, dans des colonnes distinctes:

    nom,
    prénom,
    adresse email officielle (éviter les adresses gmail, hotmail et autres autant que possible),
    date de naissance,
    institution,
    année d’étude,
    (numéro de téléphone),
    (numéro matricule).

