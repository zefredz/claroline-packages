Sachez qu'un compte utilisateur sur iCampus n'est pas automatiquement créé lors de l'inscription administrative de l'étudiant ou du membre du personnel.
Le compte iCampus est créé lors de la première tentative de connexion de l'utilisateur (après que le système ait vérifié dans la base de données centrale que ce dernier a bien le droit d'y accéder).

Si vous ne trouvez par l'étudiant que vous cherchez, c'est peut-être simplement que celui-ci ne s'est jamais authentifié sur la plateforme.

Contactez éventuellement l'étudiant afin de lui demander de se connecter sur iCampus.

Vous pouvez également utiliser l'outil d'importation des listes officielles d'étudiants... mais attention : son usage a des conséquences qui peuvent s'avérer ennuyantes.

