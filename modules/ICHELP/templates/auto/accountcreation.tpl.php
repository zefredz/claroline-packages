Si vous être inscrit(e) comme étudiant(e) à l'université, vous pouvez accéder à iCampus en utilisant votre identifiant global UCL.
Lors de votre première connexion, votre compte iCampus sera automatiquement créé.

Attention : votre compte UCL doit avoir été préalablement activé !
Si ce n'est pas le cas, commencez par le faire, en allant à cette adresse : http://www.uclouvain.be/4040.

Si vous êtes extérieur(e) à l'UCL mais que vous suivez un programme d'étude pour lequel un accès à iCampus est nécessaire, adressez une demande justifiée de création de compte extérieur à cette adresse : icampus@uclouvain.be.