Par defaut, l'espace réservé pour les documents d'un cours sur iCampus est de 100 Mo.

Si vous pensez avoir besoin d'espace supplémentaire, nous pouvons l'augmenter (dans les limites du raisonnable) en nous envoyant une demande motivée à cette adresse : icampus@uclouvain.be

Mais avant de recourrir à nos services, pensez à vérifier s'il n'est pas possible de rationnaliser l'espace dont vous disposez:

- Supprimez les documents obsolètes ou inutilisés
- Préférez le format PDF aux formats "PowerPoint" ou "Word"
- Les diaporamas étant destinés à être projetés sur un écran, inutile d'y intégrer des images de haute résolution
- Evitez les images de fond dans vos diaporamas
- Scannez vos livres en niveaux de gris... ou mieux en noir et blanc (profondeur d'échantillonnage de 1 bit)

Sachez qu'iCampus est avant tout une plateforme d'elearning, et n'a pas pour vocation de servir d'espace de stockage et/ou de backup.

Si vous voulez transmettre à vos étudiants des fichiers volumineux, utilisez de préférences le service "Transvol" de l'UCL: http://www.uclouvain.be/12496.html